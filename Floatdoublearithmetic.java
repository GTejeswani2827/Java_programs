package com.java.javacodingprograms.Datatypes;
public class Floatdoublearithmetic {
    public static void main(String[] args) {
        float f1 = 10.5f;
        float f2 = 2.5f;
        double d1 = 20.5;
        double d2 = 3.5;
        
        System.out.println("Float Addition: " + (f1 + f2));
        System.out.println("Float Subtraction: " + (f1 - f2));
        System.out.println("Float Multiplication: " + (f1 * f2));
        System.out.println("Float Division: " + (f1 / f2));
        
        System.out.println("Double Addition: " + (d1 + d2));
        System.out.println("Double Subtraction: " + (d1 - d2));
        System.out.println("Double Multiplication: " + (d1 * d2));
        System.out.println("Double Division: " + (d1 / d2));
    }
}


